SUPPORTED_LANGUAGES = list(TRANSLATIONS.keys())
DEFAULT_LANGUAGE = "en"


def get_translation(lang: str, key: str, **kwargs) -> str:
    """Get translation for a key in specified language with optional formatting."""
    if lang not in TRANSLATIONS:
        lang = DEFAULT_LANGUAGE

    text = TRANSLATIONS[lang].get(key, key)

    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass

    return text


def get_month_name(lang: str, month_num: str) -> str:
    """Get month name in specified language."""
    if lang not in TRANSLATIONS:
        lang = DEFAULT_LANGUAGE

    months = TRANSLATIONS[lang].get("months", {})
    return months.get(month_num, month_num)


def format_date_localized(lang: str, date_str: str) -> str:
    """Format date string (YYYYMMDD) to localized format."""
    if len(date_str) != 8:
        return date_str

    year = date_str[:4]
    month = date_str[4:6]
    day = date_str[6:8]

    month_name = get_month_name(lang, month)

    return f"{int(day)} {month_name} {year}"


def get_language_option(lang: str) -> str:
    """Get language option string for dropdown: '🇹🇷 Türkçe'"""
    flag = LANGUAGE_FLAGS.get(lang, "🌐")
    name = LANGUAGE_NAMES.get(lang, lang)
    return f"{flag} {name}"
